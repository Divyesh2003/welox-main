    /**
     * Get the query source of dataTable.
     */
    public function query(Inquiry $model): QueryBuilder
    {
        return $model->newQuery();
    }

    /**
     * Optional method if you want to use the html builder.
     */
    public function html(): HtmlBuilder
    {
        return $this->builder()
                    ->setTableId('inquiry-table')
                    ->columns($this->getColumns())
                    ->minifiedAjax()
                    //->dom('Bfrtip')
                    ->orderBy(1)
                    ->selectStyleSingle()
                    ->buttons([
                        Button::make('excel'),
                        Button::make('csv'),
                        Button::make('pdf'),
                        Button::make('print'),
                        Button::make('reset'),
                        Button::make('reload')
                    ]);
    }

    /**
     * Get the dataTable columns definition.
     */
    public function getColumns(): array
    {
        return [
            Column::make('id')->title('ID'),
            Column::make('name')->title('Name'),
            // Column::make('image')->title('Image'),
            Column::make('email')->title('Email'),
            // Column::make('code')->title('Code'),
            Column::make('phone')->title('Phone'),
            // Column::make('created_at')->title('Date'),
            Column::make('created_at')
                ->title('Date')
                ->format(function ($value, $column, $row) {
                    return $value->toDateString('y-m-d'); // Assuming $value is a Carbon instance
                }),
            Column::make('inquiry_solve')->title('Inquiry Solve'),
            Column::computed('action')
            ->exportable(false)
            ->printable(false)
            ->width(60)
            ->addClass('text-center'),
        ];
    }

    /**
     * Get the filename for export.
     */
    protected function filename(): string
    {
        return 'Inquiry_' . date('YmdHis');
    }
